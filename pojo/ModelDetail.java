package com.xlkj.project.modules.practice.pojo;

public class ModelDetail {
		private String practiceModelName;
		private String processAreaName;
		private String spePracticeName;
		private int  executeSequence;
		private String checkerRole;
		public String getPracticeModelName() {
			return practiceModelName;
		}
		public void setPracticeModelName(String practiceModelName) {
			this.practiceModelName = practiceModelName;
		}
		public String getProcessAreaName() {
			return processAreaName;
		}
		public void setProcessAreaName(String processAreaName) {
			this.processAreaName = processAreaName;
		}
		public String getSpePracticeName() {
			return spePracticeName;
		}
		public void setSpePracticeName(String spePracticeName) {
			this.spePracticeName = spePracticeName;
		}
		public int getExecuteSequence() {
			return executeSequence;
		}
		public void setExecuteSequence(int executeSequence) {
			this.executeSequence = executeSequence;
		}
		public String getCheckerRole() {
			return checkerRole;
		}
		public void setCheckerRole(String checkerRole) {
			this.checkerRole = checkerRole;
		}
}
